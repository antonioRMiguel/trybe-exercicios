// 1. operações aritméticas básicas (soma, subtração, multiplicação e divisão - +, -, *, /, %)

let a = 17;
let b = 7;


// Soma
let soma = a + b;
console.log(soma);

// Subtração
let subtracao = a - b;
console.log(subtracao);

// Multiplicação
let multiplicacao = a * b;
console.log(multiplicacao);

// Divisão
let divisao = a / b;
console.log(divisao);

// Módulo
let modulo = a % b;
console.log(modulo);
