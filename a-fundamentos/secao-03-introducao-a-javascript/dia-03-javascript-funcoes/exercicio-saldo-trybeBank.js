// Utilizando Funções       
// Considere a variável saldo, que representa a quantia em conta de determinada(o) cliente do TrybeBank e escreva quatro programas que:
// Adicione um valor ao saldo.
// Subtraia um valor do saldo.
// Multiplique o valor do saldo por uma taxa.
// Divida o valor do saldo.

// 1. Adicione um valor ao saldo.

const saldo = 3500; // saldo inicial

function adicionarSaldo(valor) {
    return valor + saldo;
}

console.log(saldo);

// 2. Subtraia um valor do saldo.

function subtrairSaldo(valor) {
    return saldo - valor;
}

console.log(saldo);

// 3. Multiplique o valor do saldo por uma taxa.

function multiplicarSaldo(valor) {
    return saldo * valor;
}   

console.log(saldo);

// 4. Divida o valor do saldo.

function dividirSaldo(valor) {  
    return saldo / valor;
}   

console.log(saldo);




